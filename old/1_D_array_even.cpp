#include<iostream>

using namespace std;

int main()
{
	int array[50], var=0;
	
		for(int i=1; i<=100 ; ++i)
		{	
			//even number
			if(i%2 == 0)
			{
				array[var] = i;
				++var;
			}
			
			else;
			
					
		}
		
		
		for(int j=0; j<50; ++j)
			cout<<array[j]<<endl;
}
